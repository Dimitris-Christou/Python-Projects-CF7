s = "Coding Factory"

s1 = s[7]  # F
print(s1)

# traverse / copy 
s2 = s[::]
print(s2)

# from 7th to the end
s3 = s[7:]
print(s3)

s4 = s[-1]
print(s4)

# Coding Factor
s5 = s[:-1]
print(s5)

# Factor
s6 = s[7:-1]
print(s6)

# empty string
s7 = s[7:7]
print(s7)

# yrotacF gnidoC
s8 = s[::-1]
print(s8)